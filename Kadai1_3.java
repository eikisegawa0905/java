public class Kadai1_3{
    public static void main (String [] args) {
        boolean one = true ;
        char two = '駆' ;
        double three = 3.14 ;
        long four = 314159265853979L ;
        String five = "ミナトの攻撃！敵に１５ダメージを与えた。";

        System.out.println("①" + one + "の答えはboolean型");
        System.out.println("②" + two + "の答えはchar型");
        System.out.println("③" + three + "の答えはdouble型"); 
        System.out.println("④" + four + "の答えはlong型");
        System.out.println("⑤" + five + "の答えはString型");


    }
}